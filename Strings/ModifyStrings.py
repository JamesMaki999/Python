"""
String Modifications range from changing the case of strings to replacing or splitting strings
"""

x = " Modifying, Strings! "         # Variable Declaration

#Upper Case
print(x.upper())

#Lower Case
print(x.lower())

#Remove Whitespace
print(x.strip())

#Replace String
print(x.replace("M", "J"))

#Split String
print(x.split(","))